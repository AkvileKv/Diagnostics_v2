$(document).ready(function() {
  $('.select-gray').css('color', 'gray');
  $('.select-gray').change(function() {
    var current = $('.select-gray').val();
    if (current != 'null') {
      $('.select-gray').css('color', 'black');
    } else {
      $('.select-gray').css('color', 'gray');
    }
  });
});
